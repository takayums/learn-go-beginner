package main

import "fmt"

func StringFunc() {
	fmt.Println("Muhammad")
	fmt.Println("Muhammad Asraf")
	fmt.Println("Muhammad Asraf Takayuma")

	// Len / Panjang string
	fmt.Println(len("Muhammad Asraf"))
	// Mengambil karakter
	fmt.Println("Muhammad"[0])
	fmt.Println("Muhammad Asraf"[2])
}
